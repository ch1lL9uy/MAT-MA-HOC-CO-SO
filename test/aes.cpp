void AES::addRoundKey(uint8_t state[16], size_t round) {
    for (int c = 0; c < 4; ++c) {
        uint32_t k = roundKeys_[round * 4 + c];
        state[c * 4 + 0] ^= (k >> 24) & 0xFF;
        state[c * 4 + 1] ^= (k >> 16) & 0xFF;
        state[c * 4 + 2] ^= (k >> 8) & 0xFF;
        state[c * 4 + 3] ^= k & 0xFF;
    }
}


void AES::invSubBytes(uint8_t state[16]) {
    for (int i = 0; i < 16; ++i) state[i] = inv_sbox[state[i]];
}

void AES::invShiftRows(uint8_t state[16]) {
    uint8_t temp[16];
    for (int i = 0; i < 16; ++i) temp[i] = state[i];
    for (int i = 0; i < 4; ++i) {
        for (int j = 0; j < 4; ++j) {
            state[i + 4 * j] = temp[i + 4 * ((j - i + 4) % 4)];
        }
    }
}

void AES::invMixColumns(uint8_t state[16]) {
    for (int i = 0; i < 4; ++i) {
        uint8_t a[4] = {state[i], state[i + 4], state[i + 8], state[i + 12]};
        state[i] = galoisMul14[a[0]] ^ galoisMul11[a[1]] ^ galoisMul13[a[2]] ^ galoisMul9[a[3]];
        state[i + 4] = galoisMul9[a[0]] ^ galoisMul14[a[1]] ^ galoisMul11[a[2]] ^ galoisMul13[a[3]];
        state[i + 8] = galoisMul13[a[0]] ^ galoisMul9[a[1]] ^ galoisMul14[a[2]] ^ galoisMul11[a[3]];
        state[i + 12] = galoisMul11[a[0]] ^ galoisMul13[a[1]] ^ galoisMul9[a[2]] ^ galoisMul14[a[3]];
    }
}

std::vector<uint8_t> AES::generateRandomIV() {
    std::random_device rd;
    std::mt19937 gen(rd());
    std::uniform_int_distribution<> dis(0, 255);
    std::vector<uint8_t> iv(16);
    for (auto& byte : iv) byte = dis(gen);
    return iv;
}

void AES::processECB(const uint8_t* input, uint8_t* output, size_t blocks, bool encrypt) {
    for (size_t i = 0; i < blocks; ++i) {
        uint8_t state[16];
        std::copy_n(input + i * 16, 16, state);
        if (encrypt) encryptBlock(state);
        else decryptBlock(state);
        std::copy_n(state, 16, output + i * 16);
    }
}

void AES::processCBC(const uint8_t* input, uint8_t* output, size_t blocks, const uint8_t iv[16], bool encrypt) {
    uint8_t prev[16];
    if (encrypt) {
        std::copy_n(iv, 16, prev);
        for (size_t i = 0; i < blocks; ++i) {
            uint8_t state[16];
            std::copy_n(input + i * 16, 16, state);
            xorBlock(state, prev);
            encryptBlock(state);
            std::copy_n(state, 16, output + i * 16);
            std::copy_n(state, 16, prev);
        }
    } else {
        std::copy_n(iv, 16, prev);
        for (size_t i = 0; i < blocks; ++i) {
            uint8_t block[16], state[16];
            std::copy_n(input + i * 16, 16, block);
            std::copy_n(block, 16, state);
            decryptBlock(state);
            xorBlock(state, prev);
            std::copy_n(state, 16, output + i * 16);
            std::copy_n(block, 16, prev);
        }
    }
}

void AES::processCFB(const uint8_t* input, uint8_t* output, size_t blocks, const uint8_t iv[16], bool encrypt) {
    uint8_t iv_copy[16];
    std::copy_n(iv, 16, iv_copy);
    for (size_t i = 0; i < blocks; ++i) {
        uint8_t state[16];
        std::copy_n(iv_copy, 16, state);
        encryptBlock(state);

        if (encrypt) {
            xorBlock(state, input + i * 16);
            std::copy_n(state, 16, output + i * 16);
            std::copy_n(state, 16, iv_copy);
        } else {
            uint8_t temp[16];
            std::copy_n(input + i * 16, 16, temp);
            uint8_t decrypted[16];
            std::copy_n(temp, 16, decrypted);
            xorBlock(decrypted, state);
            std::copy_n(decrypted, 16, output + i * 16);
            std::copy_n(temp, 16, iv_copy);
        }
    }
}

void AES::processOFB(const uint8_t* input, uint8_t* output, size_t blocks, 
                    const uint8_t iv[16]) {
    uint8_t iv_copy[16];
    std::copy_n(iv ? iv : generateRandomIV().data(), 16, iv_copy);
    for (size_t i = 0; i < blocks; ++i) {
        uint8_t state[16];
        std::copy_n(iv_copy, 16, state);
        encryptBlock(state);
        xorBlock(state, input + i * 16);
        std::copy_n(state, 16, output + i * 16);
        std::copy_n(state, 16, iv_copy);
    }
}

void AES::validateKeyIV(const std::vector<uint8_t>& key, const std::vector<uint8_t>& iv) const {
    if (!key.empty() && key.size() != static_cast<size_t>(keyLength_))
        throw std::invalid_argument("Invalid key length");
    if (!iv.empty() && iv.size() != 16)
        throw std::invalid_argument("Invalid IV length");
}

void AES::xorBlock(uint8_t* target, const uint8_t* source) {
    for (int i = 0; i < 16; ++i) target[i] ^= source[i];
}

void AES::padData(std::vector<uint8_t>& data) const {
    size_t padding = 16 - (data.size() % 16);
    data.insert(data.end(), padding, static_cast<uint8_t>(padding));
}

void AES::unpadData(std::vector<uint8_t>& data) const {
    if (data.empty()) return;
    size_t padding = data.back();
    if (padding > 16) throw std::runtime_error("Invalid padding");
    data.resize(data.size() - padding);
}

