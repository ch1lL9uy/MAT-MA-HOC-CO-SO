uint32_t AES::subWord(uint32_t word) {
    return ((uint32_t)sbox[(word >> 24) & 0xff] << 24) |
           ((uint32_t)sbox[(word >> 16) & 0xff] << 16) |
           ((uint32_t)sbox[(word >> 8) & 0xff] << 8) |
           (uint32_t)sbox[word & 0xff];
}

uint32_t AES::rotWord(uint32_t word) {
    return (word << 8) | (word >> 24);
}

void AES::encryptBlock(uint8_t state[16]) {
    addRoundKey(state, 0);
    for (size_t round = 1; round < rounds_; ++round) {
        subBytes(state);
        shiftRows(state);
        mixColumns(state);
        addRoundKey(state, round);
    }
    subBytes(state);
    shiftRows(state);
    addRoundKey(state, rounds_);
}

void AES::decryptBlock(uint8_t state[16]) {
    addRoundKey(state, rounds_);
    for (size_t round = rounds_ - 1; round > 0; --round) {
        invShiftRows(state);
        invSubBytes(state);
        addRoundKey(state, round);
        invMixColumns(state);
    }
    invShiftRows(state);
    invSubBytes(state);
    addRoundKey(state, 0);
}

void AES::subBytes(uint8_t state[16]) {
    for (int i = 0; i < 16; ++i) state[i] = sbox[state[i]];
}

