AES::AES(const std::vector<uint8_t>& key, Mode mode, KeyLength keyLength)
    : mode_(mode), keyLength_(keyLength), rounds_(0) {
    validateKeyIV(key, {});
    rounds_ = (static_cast<size_t>(keyLength_) == 16) ? 10 :
              (static_cast<size_t>(keyLength_) == 24) ? 12 : 14;
    keyExpansion(key);
}

std::vector<uint8_t> AES::encrypt(const std::vector<uint8_t>& plaintext, const std::vector<uint8_t>& iv) {
    validateKeyIV({}, iv);
    std::vector<uint8_t> padded = plaintext;
    padData(padded);
    std::vector<uint8_t> ciphertext(padded.size());
    size_t blocks = padded.size() / 16;
    const uint8_t* iv_ptr = iv.empty() ? nullptr : iv.data();

    switch (mode_) {
        case Mode::ECB:
            processECB(padded.data(), ciphertext.data(), blocks, true);
            break;
        case Mode::CBC:
            processCBC(padded.data(), ciphertext.data(), blocks, iv_ptr, true);
            break;
        case Mode::CFB:
            processCFB(padded.data(), ciphertext.data(), blocks, iv_ptr, true);
            break;
        case Mode::OFB:
            processOFB(padded.data(), ciphertext.data(), blocks, iv_ptr);
            break;
    }
    return ciphertext;
}

std::vector<uint8_t> AES::decrypt(const std::vector<uint8_t>& ciphertext, const std::vector<uint8_t>& iv) {
    validateKeyIV({}, iv);
    std::vector<uint8_t> plaintext(ciphertext.size());
    size_t blocks = ciphertext.size() / 16;
    const uint8_t* iv_ptr = iv.empty() ? nullptr : iv.data();

    switch (mode_) {
        case Mode::ECB:
            processECB(ciphertext.data(), plaintext.data(), blocks, false);
            break;
        case Mode::CBC:
            processCBC(ciphertext.data(), plaintext.data(), blocks, iv_ptr, false);
            break;
        case Mode::CFB:
            processCFB(ciphertext.data(), plaintext.data(), blocks, iv_ptr, false);
            break;
        case Mode::OFB:
            processOFB(ciphertext.data(), plaintext.data(), blocks, iv_ptr);
            break;
    }
    unpadData(plaintext);
    return plaintext;
}

